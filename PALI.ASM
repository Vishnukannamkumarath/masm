.MODEL SMALL
.DATA
N2 DB 10,13,"STRING  IS PALINDROME:$"
N3 DB 10,13,"STRING IS NOT PALINDROME:$"
STR DB "MALAYALAM$"
.CODE
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET STR
L:MOV AX,[SI]
CMP AL,$
JE L1
INC SI
JMP L
L1:
MOV DI,OFFSET STR
DEC SI
L2:CMP SI,DI
JL OUT
MOV AX,[SI]
MOV BX,[DI]
CMP AL,BL
JNE OUT1
DEC SI
INC DI
JMP L2
OUT:
LEA DX,N2
MOV AH,09H
INT 21H
RET
OUT1:
LEA DX,N3
MOV AH,09H
INT 21H
RET
MOV AH,4CH
INT 21H
END


